public class SumRiznDob {
    public static void main (String args[]){
        double a=4, b=7;
        System.out.println("Сумма: " + (a+b));
        System.out.println("Різниця: " + (a-b));
        System.out.println("Добуток: " + a*b);
        System.out.println("Частка їніх квадратів: " + (a*a)/(b*b));
    }
}